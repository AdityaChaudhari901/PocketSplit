type TranslationParams = Record<string, string | number>;

const interpolate = (template: string, params?: TranslationParams): string => {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (match, key: string) => String(params[key] ?? match));
};

const normalizeLocale = (locale?: string | null): SupportedAppLocale | null => {
  const language = locale?.split(/[-_]/)[0]?.toLowerCase();
  return SUPPORTED_APP_LOCALES.includes(language as SupportedAppLocale) ? (language as SupportedAppLocale) : null;
};

export const getSystemLocale = (): string | null => {
  try {
    return Intl.DateTimeFormat().resolvedOptions().locale ?? null;
  } catch {
    return null;
  }
};

export const resolveAppLocale = (appLanguage: AppLanguageCode, systemLocale = getSystemLocale()): SupportedAppLocale => {
  if (appLanguage === "system") {
    return normalizeLocale(systemLocale) ?? DEFAULT_APP_LOCALE;
  }

  return appLanguage;
};

export const translate = (locale: SupportedAppLocale, key: TranslationKey, params?: TranslationParams): string => {
  const translated = translations[locale][key] ?? translations[DEFAULT_APP_LOCALE][key];
  return interpolate(translated, params);
};

export const useTranslation = () => {
  const appLanguage = useAppStore((state) => state.appLanguage);
  const locale = useMemo(() => resolveAppLocale(appLanguage), [appLanguage]);

  return useMemo(
    () => ({
      appLanguage,
      locale,
      t: (key: TranslationKey, params?: TranslationParams) => translate(locale, key, params)
    }),
    [appLanguage, locale]
  );
};
